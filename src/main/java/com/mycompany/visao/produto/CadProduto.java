    private Boolean existeDadosTemporarios(){        
        if(DadosTemporarios.temObject instanceof ModProduto){
            int id = ((ModProduto) DadosTemporarios.temObject).getId();
            String nome = ((ModProduto) DadosTemporarios.temObject).getNome();
            
            tfId.setText(String.valueOf(id));
            tfPreco.setText(nome);
        
            DadosTemporarios.temObject = null;
            
            return true;
        }else
            return false;
    }
    
    

