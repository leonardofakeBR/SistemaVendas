    private void inserir(){
        DaoProduto daoProduto = new DaoProduto();
        
        if (daoProduto.inserir(Integer.parseInt(tfId.getText()),Integer.parseInt(tfId_Cidade.getText()), tfPreco.getText(), Integer.parseInt(tfId_marca.getText()), Integer.parseInt(tfId_categoria.getText()))){
            JOptionPane.showMessageDialog(null, "País salvo com sucesso!");
            
            tfId.setText("");
            tfId_Cidade.setText("");
            tfPreco.setText("");
            tfId_categoria.setText("");
            tfId_marca.setText("");
        }else{
            JOptionPane.showMessageDialog(null, "Não foi possível salvar o País!");
        }
    }
    
    private void alterar(){
        DaoProduto daoProduto = new DaoProduto();
        
        if (daoProduto.inserir(Integer.parseInt(tfId.getText()),Integer.parseInt(tfId_Cidade.getText()), tfPreco.getText(), Integer.parseInt(tfId_marca.getText()), Integer.parseInt(tfId_categoria.getText()))){
            JOptionPane.showMessageDialog(null, "País alterada com sucesso!");
            
            tfId.setText("");
            tfId_Cidade.setText("");
            tfPreco.setText("");
            tfId_categoria.setText("");
            tfId_marca.setText("");
        }else{
            JOptionPane.showMessageDialog(null, "Não foi possível alterar a país!");
        }
        
        ((ListProduto) Formularios.listProduto).listarTodos();
        
        dispose();
    }
    
    private void excluir(){
        DaoProduto daoProduto = new DaoProduto();
        
        if (daoProduto.excluir(Integer.parseInt(tfId.getText()))){
            JOptionPane.showMessageDialog(null, "País " + tfPreco.getText() + " excluído com sucesso!");
            
            tfId.setText("");
            tfPreco.setText("");

        }else{
            JOptionPane.showMessageDialog(null, "Não foi possível excluir o País!");
        }
        
        ((ListProduto) Formularios.listProduto).listarTodos();
        
        dispose();
    }
    
